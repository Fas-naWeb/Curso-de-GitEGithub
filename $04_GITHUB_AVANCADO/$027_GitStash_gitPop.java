public class $027_GitStash_gitPop {
	/*
	 * GIT STASH
	 *  Imagine que voce esta codificando,mais não sabe se esse codigo vai ser seu 
	 *  codigo final,Git stash e um comando que voce pode ficar guardando trechos de codigo 
	 *  onde voce pode adiciona - los depois.
	 *  Ex: Sabe aqueles momentos em que voce fica copiando e colando varios tre-
	 *  chos guardando para utilizar depois,então git stash faz uma pilha desses codigos 
	 *  ai voce resolve depois quais serao uteis para voce utilizar definitivaveme.Ou seja
	 *  so vai valer mesmo depois que voce commita-los definitivamente.
	 *  
	 *  com git stash voce tem que adicionar ( git add .),como se fosse fazer um commit,
	 *  porem voce esta adicionando os pontos de codigo numa pilha,sem comitalos. 
	 *  
	 *  SEQUENCIA DOS COMANDOS
	 *   git add -> Primeiro adiciona o arquivo
	 *   git stash -> Salva o arquivo numa lista para voce recuperalos depois.
	 *   git stash list -> Mostra uma lista dos stash salvos
	 *   git stash pop -> Retorna os arquivos para voce comita-los ou removelos
	 */
}

